extern layer_state_t layer_state;

bool encoder_update_user(uint8_t index, bool clockwise) {
    if (index == 0) { /* First encoder */
        if (clockwise) {
            // tap_code(KC_VOLU);
			tap_code16(dynamic_keymap_get_keycode(biton32(layer_state), 3, 6));
		}
        else {
            // tap_code(KC_VOLD);
			tap_code16(dynamic_keymap_get_keycode(biton32(layer_state), 4, 6));
            // tap_code(KC_VOLD);
		}
	}
    if (index == 1) { /* First encoder */
        if (clockwise) {
            // tap_code(KC_VOLU);
			tap_code16(dynamic_keymap_get_keycode(biton32(layer_state), 0, 5));
		}
        else {
            // tap_code(KC_VOLD);
			tap_code16(dynamic_keymap_get_keycode(biton32(layer_state), 1, 5));
            // tap_code(KC_VOLD);
        }
		
    }
	if (index == 2) { /* First encoder */
        if (clockwise) {
            // tap_code(KC_VOLU);
			tap_code16(dynamic_keymap_get_keycode(biton32(layer_state), 0, 6));
		}
        else {
            // tap_code(KC_VOLD);
			tap_code16(dynamic_keymap_get_keycode(biton32(layer_state), 1, 6));
            // tap_code(KC_VOLD);
        }
		
    }

    return false;
}

